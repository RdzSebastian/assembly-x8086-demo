;INGRESO UN TEXTO Y VEO SI ES CAPICUA ES DECIR PALINDROMO;

.8086
.model small
.stack 100H
.data
CARTEL DB "INGRESE TEXTO:$"
NOES DB "NO ES PALINDROMO$ "
TEXTO DB 50 DUP ('$')
ESPAL DB "ES PALINDROMO$"

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV AH,9
	MOV DX, OFFSET CARTEL
	INT 21H
	MOV BX, '0'
CICLO:
	MOV AH, 1
	INT 21H
	CMP AL, '$'
	JE SIGO
	MOV TEXTO [BX],AL
	INC BX
	CMP BX, 32H            ; ES 50 EN HEXADECIMAL	
	JE SIGO
	JMP CICLO
SIGO:
	DEC BX
	MOV DI, 0H             ; 0 EN HEXADECIMAL
RECO:
	MOV AL, TEXTO [DI]
	CMP AL, TEXTO [BX]
	JNE NOES
	INC DI
	DEC BX
	CMP BX,DI
	JLE SIES
	JMP RECO
NOES:
	MOV AH,9
	MOV DX, OFFSET NOES
	INT 21H
	JMP FIN
SIES:
	MOV AH,9
	MOV DX, OFFSET ESPAL
	INT 21H
FIN:
	MOV AX,4C00H
	INT 21H	
ENDP
END MAIN	
	
	
	
	